<h3>Policy Procurement STARDUCKS COFFEE<br /></h3>

Kebijakan STARDUCKS COFFEE dalam pengadaan barang/jasa lebih memprioritaskan pengadaan secara langsung dari pabrikan, distributor resmi atau agen tunggal dan menghindari penggunaan perantara yang tidak memberikan nilai tambah. Dalam pemilihan vendor, STARDUCKS COFFEE lebih memperhatikan aspek kemampuan dan daya saing perusahaan, daripada mempertimbangkan aspek kepemilikan TDR.<br /><br />

Ketentuan dan persyaratan bagi para rekanan yang ingin mengajukan diri sebagai rekanan STARDUCKS COFFEE, adalah sebagai berikut, memenuhi Persyaratan Administratif seperti :<br />

Badan usaha dengan dibuktikan adanya copy Akta Pendirian Perusahaan beserta perubahannya dan copy SIUP<br />
NPWP dan surat penetapan sebagai PKP<br />
Laporan Keuangan 2 tahun terakhir (Audited lebih diutamakan)<br />
Copy Sertifikasi (jika diperlukan)<br /><br />

Memenuhi persyaratan teknis seperti :<br />

Data fasilitas perusahaan yang dimiliki<br />
Data kapasitas produksi yang tersedia dan terpakai<br />
Jumlah pegawai dan keahliannya<br />
Teknologi dan Inovasi<br />
Delivery<br />
Quality Control & SOP<br />
Sertifikasi lainnya (jika diperlukan)<br />
Mempunyai kemampuan/keahlian dan pengalaman dalam bidang usahanya disertai dengan adanya daftar pengalaman kerja dan referansi.<br /><br />

Semua rekanan akan dilakukan evaluasi berdasarkan kriteria administratif dan teknis. Dalam hal ini STARDUCKS COFFEE berhak untuk melakukan penilaian dan melakukan pemeriksaan secara langsung atas fasilitas yang dimiliki oleh rekanan. Segala keputusan penilaian yang dilakukan oleh pihak STARDUCKS COFFEE adalah mutlak dan tidak dapat diganggu gugat dan tidak ada kewajiban bagi STARDUCKS COFFEE untuk memberikan penjelasan detail atas keputusan hasil penilaian.<br />

STARDUCKS COFFEE mempunyai kebijakan dan berhak dalam menetapkan jumlah vendor dalam Vendor List STARDUCKS COFFEE serta keberadaan vendor dalam Vendor List STARDUCKS COFFEE yang akan dievaluasi secara rutin.<br />

Demikian Pemberitahuan ini disampaikan untuk menjadi perhatian.</div>


