<h2>SELAMAT DATANG <br />
DI SITUS E-PROCUREMENT (Procurement Online)<br />
STARDUCKS COFFEE<br /></h2>

E-procurement adalah fasilitas yang diberikan oleh STARDUCKS COFFEE bagi para vendor (pemasok) di lingkungan STARDUCKS COFFEE.
E-procurement adalah sistem yang mudah karena menu dan aplikasi sistemnya yang jelas. Sehingga tidak terlalu lama mempelajarinya. Berbasis web, sehingga lebih menarik dan lebih sering diupdate. Dan terkoneksi ke internet untuk kemudahan akses dari mana saja dan kapan saja.

E-procurement adalah sistem pembelian yang menawarkan proses pembelian secara elektronik. Memudahkan fungsi administrasi bagi STARDUCKS COFFEE dan vendor. Tujuan dari sistem ini adalah efisiensi operasional dan penghematan potensial biaya yang terjadi. Baik bagi STARDUCKS COFFEE dan pihak vendor sendiri. Juga untuk transparansi dan kejelasan pertanggung-jawabannya.

Keunggulan procurement online ini berdasarkan pada praktek-praktek yang sudah terjadi di dunia bisnis baik di sektor swasta dan pemerintah. Ditambah keunggulan internet, sistem e-procurement ini akan meningkatkan kesempatan berusaha bagi para vendor.</div>


